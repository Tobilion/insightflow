#: Flat 1% steps with one violent day -- exactly one obvious outlier.
ANOMALY_CLOSES = [100.0, 101.0, 102.01, 103.03, 104.06, 135.0, 136.35, 137.71, 139.09]


def alpha_vantage_payload(closes, symbol="TEST", start="2026-01-05") -> dict:
    """Wrap closes in the exact envelope Alpha Vantage returns (newest first)."""
    frame = frame_from_closes(closes, start=start)
    series = {}
    for date, row in frame.iloc[::-1].iterrows():
        series[date.strftime("%Y-%m-%d")] = {
            "1. open": f"{row['open']:.4f}",
            "2. high": f"{row['high']:.4f}",
            "3. low": f"{row['low']:.4f}",
            "4. close": f"{row['close']:.4f}",
            "5. volume": str(int(row["volume"])),
        }
    return {
        "Meta Data": {"2. Symbol": symbol, "5. Time Zone": "US/Eastern"},
        "Time Series (Daily)": series,
    }


