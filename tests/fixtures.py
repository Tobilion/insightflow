"""Hand-built fixtures with known, hand-checkable answers.

Every number the analysis tests assert against can be verified with a
calculator from these series -- that is the point. Fixtures generated by the
same code under test would prove nothing.
"""

from __future__ import annotations

import pandas as pd


def frame_from_closes(closes, start="2026-01-05") -> pd.DataFrame:
    """Build an OHLCV frame on consecutive business days from close prices."""
    index = pd.bdate_range(start=start, periods=len(closes), name="date")
    series = pd.Series([float(c) for c in closes], index=index)
    return pd.DataFrame(
        {
            "open": series,
            "high": series * 1.01,
            "low": series * 0.99,
            "close": series,
            "volume": 1_000_000.0,
        }
    )


#: +10%, -50/110, ... chosen so returns are exact fractions.
